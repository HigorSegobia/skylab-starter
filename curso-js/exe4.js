function experiencia(anos) {
    if (anos <= 1) {
        console.log('Iniciante')
    } else if (anos <= 5) {
        console.log('Junior')
    } else if (anos <= 9) {
        console.log('Pleno')
    } else if (anos > 10) {
        console.log('Senior')
    } else;
}
var anosEstudo = 12;
experiencia(anosEstudo);